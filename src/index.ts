//###############################################################
import { connectDB } from "./db";
import app from "./app";
import {PORT} from './config'
//###############################################################

//###############################################################
async function main() {
    try {
        await connectDB();
        app.listen(PORT);
        console.log(`Listen on port ${PORT}`);
    } catch (e) {
        console.log(`Ocurrio un error: ${e}`);
    }
}
//###############################################################

//###############################################################
main();
//###############################################################
